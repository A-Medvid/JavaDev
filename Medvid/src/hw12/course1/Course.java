package hw12.course1;

import hw12.Entity;

public class Course extends Entity {
    private int id;
    private String name;

    public Course() {
    }

    public Course(int id) {
        this.id = id;
    }

    public Course(int id, String name) {
        this.id = id;
        this.name = name;
    }

    @Override
    public int getId() {
        return id;
    }

    @Override
    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Course{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
